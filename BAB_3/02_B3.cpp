#include <iostream>
using namespace std;

int main()
{
int X = 82, Y = 26;
cout << X << "+" << Y <<" = "<<X+Y << endl;
cout << X << "-" << Y <<" = "<<X-Y << endl;
cout << X << "*" << Y <<" = "<<X*Y << endl;
cout << X << "/" << Y <<" = "<<X/Y << endl;
cout << X << "%" << Y <<" = "<<X%Y << endl;
cout << "-" << X << " = " << -X << endl;
return 0;
}
